mv $LFS/usr/bin/chroot $LFS/usr/sbin
mkdir -p $LFS/usr/share/man/man8
mv $LFS/usr/share/man/man1/chroot.1 $LFS/usr/share/man/man8/chroot.8
sed -i 's/"1"/"8"/' $LFS/usr/share/man/man8/chroot.8

